function showSchedule() {
  var section = document.getElementById("section").value;
  var elective = document.getElementById("elective").value;
  var selectedDay = document.getElementById("day").value;
  var scheduleDiv = document.getElementById("schedule");

  // Clear previous schedule
  scheduleDiv.innerHTML = "";

  if (section.toUpperCase() === "A") {
      // Display schedule for section A based on elective and selected day
      var filteredSchedule = getFilteredSchedule(elective, selectedDay);
      displaySchedule(filteredSchedule);
  } 
  else if (section.toUpperCase() === "B") {
      scheduleDiv.innerHTML = "Please Select the Section A";
  }else {
      scheduleDiv.innerHTML = "Does'nt Exists";
  }
}

function getFilteredSchedule(elective, selectedDay) {
  var electiveKey = Object.keys(schedules).find(key => key.toLowerCase() === elective.toLowerCase());

  var schedule = electiveKey ? schedules[electiveKey][selectedDay] : [];
  return schedule || [];
}

function displaySchedule(schedule) {
  var scheduleDiv = document.getElementById("schedule");

  for (var i = 0; i < schedule.length; i++) {
      var entry = schedule[i];
      var scheduleEntry = document.createElement("p");
      scheduleEntry.textContent = entry.time + " " + entry.subject + " " + entry.venue;
      scheduleDiv.appendChild(scheduleEntry);
  }
}

